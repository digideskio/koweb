namespace skeeks\template\tshop;
use yii\web\AssetBundle;
class TshopAsset extends AssetBundle
{
    public $sourcePath = '@skeeks/template/tshop/src/HTML/';
    /**
     * @param string $asset
     * @return string
     * @throws \yii\base\InvalidConfigException
     */
    static public function getAssetUrl($asset)
    {
        return \Yii::$app->assetManager->getAssetUrl(\Yii::$app->assetManager->getBundle(static::className()), $asset);
    }
}